class Pared(pygame.sprite.Sprite):
    def __init__(self, x, y):
        super().__init__()
        self.image = pygame.Surface([TILE, TILE])
        self.image.fill(AZUL)
        self.rect = self.image.get_rect(topleft=(x, y))


def crear_laberinto():
    lista_paredes = pygame.sprite.Group()
    for fila_index, fila in enumerate(matriz_mapa):
        for col_index, valor in enumerate(fila):
            if valor == 1:
                lista_paredes.add(Pared(col_index * TILE, fila_index * TILE))
    return lista_paredes


def crear_items(matriz=matriz_mapa):
    puntos_grupo = pygame.sprite.Group()
    powerups_grupo = pygame.sprite.Group()
    for fila_index, fila in enumerate(matriz):
        for col_index, valor in enumerate(fila):
            x = col_index * TILE
            y = fila_index * TILE
            if valor == 0:
                puntos_grupo.add(Punto(x, y))
            elif valor == 2:
                powerups_grupo.add(PowerUp(x, y))
    return puntos_grupo, powerups_grupo
